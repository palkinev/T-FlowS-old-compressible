!======================================================================!
  SUBROUTINE SwapR(a,b) 
!----------------------------------------------------------------------!
!   Swaps two double precision reals.                                  !
!----------------------------------------------------------------------!
  IMPLICIT NONE
!-----------------------------[Parameters]-----------------------------!
  REAL :: a,b
!-------------------------------[Locals]-------------------------------!
  REAL :: t
!--------------------------------[CVS]---------------------------------!
!  $Id: SwapR.f90,v 1.5 2002/10/30 16:29:22 niceno Exp $  
!  $Source: /home/muhamed/.CVSROOT/T-Rex/Generate/SwapR.f90,v $  
!======================================================================!

  t=a
  a=b
  b=t

  END SUBROUTINE SwapR
