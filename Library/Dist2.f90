!======================================================================!
  REAL FUNCTION Dist2(xA,yA,zA,xB,yB,zB)
!----------------------------------------------------------------------!
!  Calculates distance between two points.                             !
!----------------------------------------------------------------------!
  IMPLICIT NONE
!-----------------------------[Parameters]-----------------------------!
  REAL :: xA,yA,zA,xB,yB,zB
!--------------------------------[CVS]---------------------------------!
!  $Id: Dist2.f90,v 1.4 2002/10/30 16:29:31 niceno Exp $  
!  $Source: /home/muhamed/.CVSROOT/T-Rex/Library/Dist2.f90,v $   
!======================================================================!

  Dist2=(xA-xB)*(xA-xB) + (yA-yB)*(yA-yB) + (zA-zB)*(zA-zB)

  END FUNCTION Dist2
