!======================================================================!
  REAL FUNCTION DistR(rA,zA,rB,zB)
!----------------------------------------------------------------------!
!  Calculates distance between two points.                             !
!----------------------------------------------------------------------!
  IMPLICIT NONE
!-----------------------------[Parameters]-----------------------------!
  REAL :: rA,zA,rB,zB
!--------------------------------[CVS]---------------------------------!
!  $Id: Dist.f90,v 1.5 2002/10/30 16:29:31 niceno Exp $  
!  $Source: /home/muhamed/.CVSROOT/T-Rex/Library/Dist.f90,v $  
!======================================================================!

  DistR=sqrt( (rA-rB)*(rA-rB) + (zA-zB)*(zA-zB) )

  END FUNCTION DistR
